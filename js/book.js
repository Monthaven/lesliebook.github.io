function splitIntoPages(chapters, charsPerPage) {
    const pages = [];
    
    chapters.forEach(chapter => {
        const words = chapter.content.split(' ');
        let currentPage = '';
        let isFirstPage = true;
        
        words.forEach(word => {
            if ((currentPage + ' ' + word).length > charsPerPage) {
                pages.push({
                    isChapterStart: isFirstPage,
                    chapter: isFirstPage ? chapter : null,
                    content: currentPage.trim()
                });
                currentPage = word;
                isFirstPage = false;
            } else {
                currentPage += ' ' + word;
            }
        });
        
        if (currentPage.trim()) {
            pages.push({
                isChapterStart: isFirstPage,
                chapter: isFirstPage ? chapter : null,
                content: currentPage.trim()
            });
        }
    });
    return pages;
}

